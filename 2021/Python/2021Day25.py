def iterate(array):
    nextArray = np.full(array.shape, ".")
    
    rows, cols = nextArray.shape
    
    moveCount = 0    
    
    # East movements
    for row in range(rows):
        for col in range(cols - 1, -1, -1):
            thisSpace = array[row, col]    
            if thisSpace in [".", "v"]:
                continue
            
            nextCol = (col + 1) % cols
            nextSpace = array[row, nextCol]
            
            if nextSpace == ".":
                nextArray[row, nextCol] = ">"
                moveCount += 1
            else:
                nextArray[row, col] = ">"

    # South movements
    for row in range(rows - 1, -1, -1):
        for col in range(cols):
            thisSpace = array[row, col]    
            if thisSpace in [".", ">"]:
                continue
            
            nextRow = (row + 1) % rows
            nextSpace = nextArray[nextRow, col]
            nextSpaceOriginal = array[nextRow, col]
            
            if nextSpace == "." and nextSpaceOriginal != "v":
                nextArray[nextRow, col] = "v"
                moveCount += 1
            else:
                nextArray[row, col] = "v"
    
    return nextArray, moveCount

